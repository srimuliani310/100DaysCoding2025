public class Main {
    public static void main(String[] args) {
       /* Constanta (Final Variabel) adalah Variabel yang
        yang nilainya tetap (tidak bisa diubah lagi) setelah
        didefinisikan.
        */
        final double phi = 3.14;
        final int detikPerMenit = 60; 
        System.out.println("Nilai phi : " + phi);
        System.out.println("Jumlah detik dalam 1 menit : " + detikPerMenit + " detik");

    }
}
