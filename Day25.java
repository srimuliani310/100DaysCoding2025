public class Day25 {
    public static void main(String[] args){
        byte a = 2;
        ++a; // Increment (Ditambah 1)
        System.out.println("Setelah Increment = " + a);
        
        --a; // Decrement (Dikurang 1)
        System.out.println("Setelah Decrement = " + a);
        
    }
}
